package Composition;

public class Resolution {
    // instance vars
    private int width;
    private int height;

    // constructors
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    // instance vars
    public int getWidth() {
        return this.width;
    }

    public int getHeight() {
        return this.height;
    }
}